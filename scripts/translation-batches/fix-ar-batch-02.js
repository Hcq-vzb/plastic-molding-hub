function cleanLatinMix(text) {
  return text
    .replace(/جياهوانxiang/g, 'جياهوانشيانغ')
    .replace(/ونtchou/g, 'ونtchou')
    .replace(/ونtchou/g, 'ونtchou')
    .replace(/(\u0648)ntchou/g, '$1\u0646\u062a\u0634\u0648')
    .replace(/ntchou/g, '\u0648\u0646\u062a\u0634\u0648')
    .replace(/تشen وijun/g, '\u062a\u0634\u0646 \u0648\u064a\u062c\u0648\u0646')
    .replace(/تشen زورong/g, '\u062a\u0634\u0646 \u0632\u0648\u0631\u0648\u0646\u063a')
    .replace(/تشian قiang/g, '\u062a\u0634\u064a\u0627\u0646 \u062a\u0634\u064a\u0627\u0646\u063a')
    .replace(/شوتchou، تشang كe/g, '\u0634\u0648\u062a\u0634\u0648\u060c \u062a\u0634\u0627\u0646\u063a \u0643\u0647')
    .replace(/شوتchou تشang/g, '\u0634\u0648\u062a\u0634\u0648\u060c \u062a\u0634\u0627\u0646\u063a \u0643\u0647')
    .replace(/شوتchou/g, '\u0634\u0648\u062a\u0634\u0648')
    .replace(/البلdية/g, '\u0627\u0644\u0628\u0644\u062f\u064a\u0629')
    .replace(/بلadna/g, '\u0628\u0644\u0627\u062f\u0646\u0627')
    .replace(/وقaie/g, '\u0627\u0644\u0648\u0642\u0627\u064a\u0629')
    .replace(/وقaie/g, '\u0627\u0644\u0648\u0642\u0627\u064a\u0629')
    .replace(/الالتنmية/g, '\u0627\u0644\u062a\u0646\u0645\u064a\u0629')
    .replace(/للالبلastيك/g, '\u0644\u0644\u0628\u0644\u0627\u0633\u062a\u064a\u0643')
    .replace(/للالبلاستيك/g, '\u0644\u0644\u0628\u0644\u0627\u0633\u062a\u064a\u0643');
}

